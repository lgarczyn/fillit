void					id_tetri(t_tetri *tetri)
{
	t_coord				i;

	i = 0;
	while (i < TET_COUNT)
	{
		if (ft_memcmp(g_all_tetris[i], tetri->data, 4 * 4) == 0)
		{
			tetri->id = i;
			return ;
		}
	}
}
