#include "libft.h"

void	ft_putnbr(int n)
{
	long unsigned i;

	i = (long unsigned)n;
	if (n < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + '0');
}
