#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long unsigned i;

	i = (long unsigned)n;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		i = -i;
	}
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
		ft_putnbr_fd(i % 10, fd);
	}
	else
		ft_putchar_fd(i + '0', fd);
}
